def rsa_weak_key_attack(given_public_key_N: int, given_public_key_e: int, public_key_list: typing.List[int]) -> int:
    # Source: https://factorable.net/weakkeys12.extended.pdf
    # d = e^-1 mod (p-1)(q-1)
    # When p and q are unknown, the most efficient known method to calculate the private key
    # is to factor N into p and q and use the above equation to
    # calculate d 

    # Calculate GCDs to find one with a shared prime
    for N_item in public_key_list:
        p = gcd(given_public_key_N, N_item)

        # Check for one with a shared prime
        if ( p > 1 ):
            q = given_public_key_N // p
            phi = (p-1) * (q-1)
            d = pow(given_public_key_e, -1, phi)

    return d

