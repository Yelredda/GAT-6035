# The code for the Extended Euclidean Algorithm is adapted from the following website
# https://en.wikipedia.org/wiki/Extended_Euclidean_algorithm access date 10/21
def extended_gcd(a, b):
    s = 0
    r = b
    old_s = 1
    old_r = a
    
    while ( r != 0 ):
        quotient = old_r // r
        old_r, r = r, old_r - quotient * r
        old_s, s = s, old_s - quotient * s

    if ( b != 0 ):
        bezout_t = (old_r - old_s * a) // b
    else:
        bezout_t = 0
    
    return old_r, old_s, bezout_t # gcd, coefficient1, coefficient2

# gcd modifed from "The mechanics of compromising low entropy RSA keys" accessed 10/25
# source: https://media.defcon.org/DEF%20CON%2029/DEF%20CON%2029%20presentations/Austin%20Allshouse%20-%20The%20Mechanics%20of%20Compromising%20Low%20Entropy%20RSA%20Keys.pdf
def gcd (a, b): 
    # Modified to avoid recursive call
    while (b != 0):
        a, b = b, a % b
    return a
    
# The code for the Vigenere encryption and decryption is adapted from the following website listed in the problem description
# https://en.wikipedia.org/wiki/Vigen%C3%A8re_cipher access date 10/18
# 
# Specifically:
#
# Vigenere can also be described algebraically. If the letters A–Z are taken to be the numbers 0–25, and addition is performed modulo 26, 
# Vigenere encryption E using the key K can be written as Ci = Ek(Mi) = (Mi + Ki) mod 26 
# and decryption D using the key K can be written as Mi = Dk(Ci) = (Ci - Ki) mod 26
# M = M1... Mn message
# C = C1... Cn ciphertext
# K = K1... Kn key obtained by repeating the keyword [n/m] times in which m is the keyword length

# ---------- PASSED TEST ---------- 
def vigenere_decrypt_cipher(c: str, keyword: str) -> str:

    # -------- Create Keyword --------
    
    # keyword has to match the length of m, so needs to be expanded before use
    temp_keyword = []

    stripped_c = [] # Stripping out all special chars and converting to uppercase
    for character in c:
        if ( character.isalpha() ):
            stripped_c.append( character.upper() )

    c = "".join(stripped_c)

    i = 0 # keeping track of temp_keyword index
    for character in c:
        temp_keyword.append( keyword[i % len(keyword)] ) # mod for cycling like going through a set of cards
        i = i + 1

    temp_keyword = ''.join(temp_keyword)
    
    # -------- Decryption Logic --------
    decrypted_m = []

    for i, character in enumerate(c):
        delta = (ord(character) - (ord(temp_keyword[i]) ) ) % 26 
        cipher = chr( delta + ord('A'))
        decrypted_m.append(cipher)

    m = ''.join(decrypted_m)
    return m

