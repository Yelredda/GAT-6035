def vigenere_encrypt_message(m: str, keyword: str) -> str:

    # -------- Create Keyword --------

    # keyword has to match the length of m, so needs to be expanded before use
    temp_keyword = []

    stripped_m = [] # Stripping out all special chars and converting to uppercase
    for character in m:
        if ( character.isalpha() ):
            stripped_m.append( character.upper() )

    m = "".join(stripped_m)

    i = 0 # keeping track of temp_keyword index
    for character in m:
        temp_keyword.append( keyword[i % len(keyword)] ) # mod for cycling like going through a set of cards
        i = i + 1

    temp_keyword = ''.join(temp_keyword)
    
    # -------- Encryption Logic --------
    encrypted_m = []

    for i, character in enumerate(m):
        delta = (ord(character) + (ord(temp_keyword[i]) ) ) % 26 
        cipher = chr( delta + ord('A'))
        encrypted_m.append(cipher)

    c = ''.join(encrypted_m)

    return c

