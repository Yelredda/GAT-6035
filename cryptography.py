def rsa_broadcast_attack(N_1: int, c_1: int, N_2: int, c_2: int, N_3: int, c_3: int) -> int:
    # Source: https://en.wikipedia.org/wiki/Coppersmith%27s_attack
    # Date accessed: 10/26
    
    # c = m^e mod N
    # m = x^e mod N

    # Chinese Remainder referenced by Hastad's broadcast attack
    N_tot = N_1 * N_2 * N_3
    Inv_1 = pow( N_tot // N_1, -1, N_1 )
    Inv_2 = pow( N_tot // N_2, -1, N_2 )
    Inv_3 = pow( N_tot // N_3, -1, N_3 )

    C_factor1 = c_1 * (N_tot // N_1) * Inv_1
    C_factor2 = c_2 * (N_tot // N_2) * Inv_2
    C_factor3 = c_3 * (N_tot // N_3) * Inv_3  
    temp = ( C_factor1 + C_factor2 + C_factor3 ) % N_tot 

    # Not sure if right, but assuming that the result will be an int lets me binary search for the cube root...
    low, high = 0, temp
    while low < high: 
        mid = (low + high) // 2 + 1
        if mid * mid * mid <= temp: # e = 3 per problem description
            low = mid
        else:
            high = mid - 1

    m = low

    return m

